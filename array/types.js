// valueOf (consulta o valor do array)

var carros = ["ka", "Corsa", "Palio"];

var valueCarros = carros.valueOf();

console.log(valueCarros);

// toString converte o array em string

var arrayString = carros.toString();

console.log(arrayString);

// length mostra o tamanho do array

var tamanho = carros.length;

console.log(tamanho);
