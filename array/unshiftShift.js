// unshift adiciona um novo elemento no inicio do array

var carros = [];

carros[0] = "ka";
carros[2] = "Corsa";
carros[1] = "Palio";

carros.unshift("Gol");

console.log(carros);

// shift adiciona um novo elemento no inicio do array

var carros = [];

carros[0] = "ka";
carros[2] = "Corsa";
carros[1] = "Palio";

carros.shift();

console.log(carros);
